export const availableLanguages = Object.keys(translations).map(key => ({
    code: key,
    name: translations[key].name,
    shortName: translations[key].shortName
}));

export function useTranslation() {
    const [lang, setLang] = useLocalStorage("lifebattery_lang", null);

    // Auto-detect language if not set
    useEffect(() => {
        if (!lang) {
            const browserLang = navigator.language.split("-")[0];
            if (translations[browserLang]) {
                setLang(browserLang);
            } else {
                setLang("en"); // Fallback
            }
        }
    }, [lang, setLang]);

    // Use effective language (fallback to 'en' or 'zh' if null during initial render)
    const currentLang = lang || "en";

    const t = (key) => {
        return translations[currentLang]?.[key] || translations["en"]?.[key] || key;
    };

    return { t, lang: currentLang, setLang, availableLanguages };
}
